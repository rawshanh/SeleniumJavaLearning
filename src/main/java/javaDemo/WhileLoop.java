package javaDemo;

public class WhileLoop {

	public static void main(String[] args) {

		int i=1;
		while (i<=9) {
			System.out.println(i);
			i++; 	//i=i+1
		}
		System.out.println("*******************");
		
		int j=5;
		while (j<=4) {
			System.out.println(j);
			j++; 	//i=i+1
		}
	}

}
